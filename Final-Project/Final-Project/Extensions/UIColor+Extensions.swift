import UIKit

extension UIColor {
    @nonobjc class var black80: UIColor {
        return UIColor(white: 22.0 / 255.0, alpha: 0.8)
    }
    @nonobjc class var wishGrey: UIColor {
        return UIColor(white: 55.0 / 255.0, alpha: 1)
    }
    @nonobjc class var black: UIColor {
        return UIColor(white: 21.0 / 255.0, alpha: 1.0)
    }
    @nonobjc class var apple: UIColor {
        return UIColor(red: 93.0 / 255.0, green: 197.0 / 255.0, blue: 52.0 / 255.0, alpha: 1.0)
    }
    @nonobjc class var blackTwo: UIColor {
        return UIColor(white: 45.0 / 255.0, alpha: 1.0)
    }
    @nonobjc class var warmGrey: UIColor {
        return UIColor(white: 119.0 / 255.0, alpha: 1.0)
    }
    @nonobjc class var blackThree: UIColor {
        return UIColor(white: 29.0 / 255.0, alpha: 1.0)
    }
    @nonobjc class var white50: UIColor {
        return UIColor(white: 1.0, alpha: 0.5)
    }
}
